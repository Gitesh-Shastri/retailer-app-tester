package com.medicento.retailerappmedi.data;

public class WebCodes {

    String codes, agent, user_os;

    public WebCodes() {
    }

    public String getCodes() {
        return codes;
    }

    public WebCodes setCodes(String codes) {
        this.codes = codes;
        return this;
    }

    public String getAgent() {
        return agent;
    }

    public WebCodes setAgent(String agent) {
        this.agent = agent;
        return this;
    }

    public String getUser_os() {
        return user_os;
    }

    public WebCodes setUser_os(String user_os) {
        this.user_os = user_os;
        return this;
    }
}
